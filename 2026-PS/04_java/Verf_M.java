public class Verf_M{
    public static void maiorNumero(int a, int b){
        if (a > b){
            System.out.println(a);
        }else{
            System.out.println(b);
        }
    }
    public static void main(String[] args) {
        maiorNumero(10, 20);
        maiorNumero(50, 5);
        maiorNumero(30, 30);
    }
}